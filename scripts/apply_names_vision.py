def apply(dry_run: bool) -> None:
    lines = ["BEGIN;"]
    for item_number, name in NAMES.items():
        safe = name.replace("'", "''")
        lines.append(
            f"UPDATE products SET name='{safe}' "
            f"WHERE item_number='{item_number}' AND name LIKE 'Item %';"
        )
    lines.append("COMMIT;")
    sql = "\n".join(lines)

    if dry_run:
        print(f"[DRY RUN] {len(NAMES)} UPDATE statements generated")
        print("First 5:")
        for ln in lines[1:6]:
            print(" ", ln)
        return

    result = subprocess.run(
        ["ssh", PI_HOST,
         "sudo docker exec -i fims-postgres-1 psql -U fims -d fims --set ON_ERROR_STOP=1"],
        input=sql,
        capture_output=True,
        text=True,
        encoding="utf-8",
    )
    if result.stdout:
        print(result.stdout)
    if result.stderr:
        print("STDERR:", result.stderr)
    if result.returncode != 0:
        print(f"ERROR: psql exited {result.returncode}")
        sys.exit(1)
    else:
        print(f"Done. {len(NAMES)} items attempted.")


if __name__ == "__main__":
    dry = "--dry-run" in __import__("sys").argv
    apply(dry)
